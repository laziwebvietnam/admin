if(!function_exists('sub_text'))
{
    function sub_text($detail,$maxlen,$minlen=10)
    {
        $d=$detail;
        $detail=substr($detail,0,$maxlen);
        $details=str_split($detail);
        $i=count($details);
        if($i==$maxlen) 
        {
            $i--;
            for(;$i>$minlen;$i--)
            {
                if($details[$i]==' ')
                    break;
            }
        }
        $detail=substr($detail,0,$i);
        return $detail=$detail==$d?$detail:$detail.' ...';
    }
